import lombok.Data;

@Data
@Schema(description = "Учётные данные для входа")
public class LoginDto {

    @NotBlank
    @Size(min = 3, max = 50)
    private String username;

    @NotBlank
    @Size(min = 6, max = 100)
    private String password;
}
